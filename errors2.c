/**
 * remove_comments - This function is used to remove the comments
 * @buf: the string address to be addressed
 *
 * Return: the removed comment strinfg
 */
void remove_comments(char *buf)
{
	int i;

	for (i = 0; buf[i] != '\0'; i++)
		if (buf[i] == '#' && (!i || buf[i - 1] == ' '))
		{
			buf[i] = '\0';
			break;
		}
}

